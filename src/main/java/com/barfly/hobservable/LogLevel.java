package com.barfly.hobservable;

/**
 * The Log Levels used to determine the level that an event is logged at in the logger.
 * @author dblickstein
 */
public enum LogLevel {
    /**
     * The log level DEBUG
     */
    DEBUG,
    /**
     * The log level INFO
     */
    INFO,
    /**
     * The log level TRACE
     */
    TRACE,
    /**
     * The log level WARN
     */
    WARN,
    /**
     * The log level ERROR
     */
    ERROR;
}
